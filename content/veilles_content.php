                <h2 class="veille__main-title">git et GitHub</h2>
                <section class="veille__section">
                    <h3 class="veille__sub-title">git</h3>
                    <div class="veille__text">
                        <p>Git est un logiciel open source gratuit pour le contrôle de version distribué : il permet de suivre les modifications de n'importe quel ensemble de fichiers. Il est généralement utilisé pour coordonner le travail des programmeurs qui développent en collaboration le code source pendant le développement de logiciels.</p>
                        <ol>
                            <li>1) Il est capable de vous donner la possibilité de contrôler différentes versions de votre projet (grâce à la création de branches).</li>
                            <li>2) Il est capable de permettre à votre équipe de travailler ensemble sur le même projet au même moment (grâce à l'utilisation opportune des commandes “git push” et “git pull”).</li>
                        </ol>
                        <span>Pour commencer à l'utiliser, vous devez le télécharger et l'installer :</span>
                        <a href="https://git-scm.com/downloads">https://git-scm.com/downloads</a>
