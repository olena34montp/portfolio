                            <img src="assets/img/inst-git.png" alt="Téléchargement de git">
                            <img src="assets/img/console-git.png" alt="Utilisation de la console">
                        </div>
                    </div>
                </section>
                <section class="veille__section">
                    <h3 class="veille__sub-title">GitHub</h3>
                    <div class="veille__text">
                        <p>GitHub, Inc. est un service d'hébergement Internet pour le développement de logiciels et le contrôle de version à l'aide de Git. Il fournit le contrôle de version distribué de Git ainsi que le contrôle d'accès, le suivi des bogues, les demandes de fonctionnalités logicielles, la gestion des tâches, l'intégration continue et les wikis pour chaque projet. </p>
                        <p>Pour faciliter l'utilisation de git, ils ont créé le programme GitHub Desktop (pour ceux qui ne sont pas encore à l'aise avec le terminal).</p>
                        <span><strong>GitHub Desktop</strong> vous pouvez le télécharger à ce lien :</span>
